import { expect, test } from '@playwright/test'

test('教师可创建填空题并按知识点自动组题', async ({ page }) => {
  const suffix = Date.now().toString().slice(-6)
  await page.goto('/login')
  await page.locator('input.el-input__inner').nth(0).fill('800001')
  await page.locator('input.el-input__inner').nth(1).fill('123456')
  await page.getByRole('button', { name: '进入系统' }).click()
  await page.waitForURL('**/dashboard')

  await page.goto('/exam/questions/create')
  await page.getByLabel('题目编码').fill(`TK-${suffix}`)
  await page.getByLabel('学科').fill('语文')
  await page.locator('.grid-three .el-form-item').nth(2).locator('.el-select').click()
  await page.locator('.el-select-dropdown__item').filter({ hasText: '填空题' }).last().click()
  await page.getByLabel('知识点').fill('现代文阅读')
  await page.getByLabel('题干（纯文本）').fill('请依次填写作者、作品。')
  await page.getByLabel('题干富文本 HTML').fill('<p>请依次填写<strong>作者</strong>、作品。</p>')
  await expect(page.getByText('题干富文本预览')).toBeVisible()
  await page.getByLabel('选项 / 填空位 JSON').fill('["第1空","第2空"]')
  await page.getByLabel(/答案说明：/).fill('鲁迅|呐喊')
  await page.getByRole('button', { name: '保存题目' }).click()
  await page.waitForURL('**/exam/questions')
  await expect(page.getByText(`TK-${suffix}`)).toBeVisible()

  await page.getByRole('button', { name: '按知识点自动组题' }).click()
  const knowledgeDialog = page.getByRole('dialog', { name: '按知识点自动组题' })
  await knowledgeDialog.getByLabel('学科').fill('语文')
  await knowledgeDialog.getByLabel('知识点配额').fill('现代文阅读,1')
  await knowledgeDialog.getByRole('button', { name: '生成候选题组' }).click()
  await expect(page.getByText('已按知识点生成')).toBeVisible()
  await expect(knowledgeDialog.getByRole('cell', { name: '现代文阅读' }).first()).toBeVisible()
})
